    private void AskOpenStreetmapjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AskOpenStreetmapjButtonActionPerformed
        try {
            String location = this.LocationTextField.getText().trim();
            if (!location.isEmpty()) {
                this.response.clear();
                this.SavedjLabel.setText("____");
                Vector<JsonNode> response = OwnUtils.AskAboutTheLatLOnTheOpenstreetmap(location);
                Vector<String> elements = new Vector();

                Iterator<JsonNode> iter = response.iterator();
                JsonNode node;
                String display_name;
                while (iter.hasNext()) {
                    node = iter.next();
                    display_name = node.get("display_name").asText();
                    this.response.add(node);
                    elements.add(display_name);                    
                }
                DynamicList4Addresses listwindow = new DynamicList4Addresses(elements, this);
                listwindow.pack();
                listwindow.setVisible(true);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_AskOpenStreetmapjButtonActionPerformed

