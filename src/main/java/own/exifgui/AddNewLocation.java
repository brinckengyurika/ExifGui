    private void ChecktheinputfieldEmptyness() {
        if( (!this.NameTextField.getText().trim().isEmpty()) &&
            (!this.LattitudeTextField.getText().trim().isEmpty()) &&
            (!this.LongitudeTextField.getText().trim().isEmpty())) {
                if (OwnUtils.IsDouble(this.LattitudeTextField.getText().trim()) && 
                    OwnUtils.IsDouble(this.LongitudeTextField.getText().trim())) {
                    this.SavejButton.setEnabled(true);
                    return;
                }
        }
        this.SavejButton.setEnabled(false);
    }
    
    public void setSelectedLatLonObj(int selected) {
        JsonNode snode = this.response.get(selected);
        this.LocationTextField.setText(snode.get("display_name").asText());
        this.LattitudeTextField.setText(snode.get("lat").asText());
        this.LongitudeTextField.setText(snode.get("lon").asText());
        this.ChecktheinputfieldEmptyness();
    }
    
    
    
    
